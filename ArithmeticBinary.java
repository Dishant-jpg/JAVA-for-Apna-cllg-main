import java.util.*;
public class ArithmeticBinary {
   public static void main(String args[]){
    //Arithmatic of the Operators 
    // in Binary Operands is Add,Substract,Multi,divide,Modulo.
    int A = 10;
    int B = 5;
    System.out.println(" Addition of : " + (A + B)); //Add
    System.out.println(" Substration of : " + (A - B)); //Substract
    System.out.println(" Multiplication of : " + (A * B)); //Multiply
    System.out.println(" Division of : " + (A / B)); //Divisoion
    System.out.println(" Modulo(Remindar) of : " + (A % B)); //Moulo
    
   } 
}
