public class Assignment {
    public static void main(String args[]){
        int A = 10, B = 5, C = 56, D = 6, E = 9;
        A += 10;  // A = A + 10 
        B -= 10;  // B = B - 10  
        C *= 10;  // C = C * 10  
        D /= 10;  // D = D / 10 
        E %= 10;  // E = E % 10 
        System.out.println(A);
        System.out.println(B);
        System.out.println(C);
        System.out.println(D);
        System.out.println(E);
        
    }
}
